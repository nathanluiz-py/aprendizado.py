"""
Operadores de atreibuicoes
+= , -= , *= , /= , //= , **= , %=

"""

contador = 10
contador = 2 #igual novo valor
print(contador)


contador = 10
contador *= 2 #multiplicacao
print(contador)

contador = 10
contador -= 2 #adicao
print(contador)


contador = 10
contador -= 2 #subtracao
print(contador)

contador = 10
contador /= 2 #divisao com ponto
print(contador)

contador = 10
contador //= 2 #divisao em inteiro
print(contador)

contador = 10 
contador **= 2  #potenciacao
print(contador)

contador = 10
contador %= 2  #resto de divisao
print(contador)