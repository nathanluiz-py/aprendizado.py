# tipos int e float 
# int -> numero inteiro
# tipo intrepresenta qualquer numero inteiro
print(11) #int
print(-11) #int

# float -> numeros com ponto flutanuante
# float representa qualquer numero
# na programacao para separar numeros ultiliza pontos nunca virgulas
print(1.1,10.10,'numeros decimais',sep=',')

# a funcao type mostra o tipo da variavel
# int,float,str
print( type(1)) #int
print( type('teste')) #str
print( type(1.1)) #float
